package com.android.sooz.arrayimage.models;

public class Projects {

    public String name;
    public int drawableID;
    public String type;
    public String designer;
    public String patternLink;

    public Projects(String name, int drawableID, String type, String designer, String patternLink){
        this.name = name;
        this.drawableID = drawableID;
        this.type = type;
        this.designer = designer;
        this.patternLink = patternLink;
    }
}
